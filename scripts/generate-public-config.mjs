import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const envPath = resolve(root, '.env');
const outPath = resolve(root, 'scripts', 'public-config.js');

function loadEnv(filePath) {
  const env = {};
  if (!existsSync(filePath)) return env;

  for (const line of readFileSync(filePath, 'utf8').split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    env[key] = value;
  }
  return env;
}

function sentryLoaderKey(dsn) {
  const match = /^https?:\/\/([^@]+)@/.exec(dsn);
  return match ? match[1] : '';
}

const env = loadEnv(envPath);
const required = ['SUPABASE_URL', 'SUPABASE_ANON_KEY', 'CALENDLY_URL', 'SENTRY_DSN'];
const missing = required.filter((key) => !env[key]);

if (missing.length) {
  console.error(
    `generate-public-config: Fehlende Variablen in .env: ${missing.join(', ')}\n` +
      'Kopiere .env.example nach .env und trage die Werte ein.'
  );
  process.exit(1);
}

const config = {
  supabaseUrl: env.SUPABASE_URL,
  supabaseAnonKey: env.SUPABASE_ANON_KEY,
  calendlyUrl: env.CALENDLY_URL,
  sentryDsn: env.SENTRY_DSN,
  sentryLoaderKey: sentryLoaderKey(env.SENTRY_DSN),
};

if (!config.sentryLoaderKey) {
  console.error('generate-public-config: SENTRY_DSN ist ungültig (Loader-Key nicht parsebar).');
  process.exit(1);
}

const contents =
  '/* Auto-generated by scripts/generate-public-config.mjs — nicht manuell bearbeiten. */\n' +
  'window.RAIS_PUBLIC_CONFIG = ' +
  JSON.stringify(config, null, 2) +
  ';\n';

writeFileSync(outPath, contents, 'utf8');
console.log('Wrote scripts/public-config.js');
